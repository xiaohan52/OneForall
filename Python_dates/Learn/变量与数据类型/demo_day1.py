# 变量与数据类型

# 普通类型
a1 = 16
a2 = 12.35
a3 = '摸鱼'
a4 = True
print(type(a1))
print(type(a2))
print(type(a3))
print(type(a4))

# 高级类型
b = ()  # 元组类型
print(type(b))
c = []  # 列表类型
print(type(c))
d = {}  # 字典类型
print(type(d))


# 变量的命名规则
# 变量必须使用a-z，A-Z，或者下划线_来命名
# 其他字符可以是数字，字母以及下划线
# 变量的命名不能使用数字来开头
# 命名不能使用关键字来命名，例如Ture

UserName = '陈文星'
user_name = '谢佩涵'
userName = '张三'

print(UserName)
print(user_name)
print(userName)
